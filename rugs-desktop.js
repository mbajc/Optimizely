    function rugsLightingRoomStyle() {

        var lightEnvSkus = window.envLightingSkus;
        var rugsEnvSkus = window.envRugsSkus;
        var $roomStyle = document.getElementById('roomStyle');

        var allSkus = window.isRugs ? rugsEnvSkus : lightEnvSkus;

        __.addClass($roomStyle, 'hide');

        for (var i = 0, leni = allSkus.length; i < leni; i++) {

            var foundIt = allSkus[i].skus.indexOf(window.foundSku) !== -1;

            if (!isAzulejoGreyMoroccanStyleRug()) {
                if (foundIt) {
                    __.removeClass($roomStyle, 'hide');

                    var $roomStyleItems = document.getElementById('roomStyleItems');
                    if ($roomStyleItems) {
                        var $rommStyleActions = document.getElementById('roomStyleActions');
                        var imgs = allSkus[i].items;
                        var imgsLen = imgs.length;

                        for (var k = 0; k < imgsLen; k++) {
                            $roomStyleItems.insertAdjacentHTML('beforeend', getRugsHtml(imgsLen, imgs[k], imgs));
                        }

                        if (imgsLen > 4) {
                            __.removeClass($rommStyleActions, 'hide');
                        }
                    }
                }
            }
        }

        galleryHeight();
        addRoomStyleEvents();
    }

    function addRoomStyleEvents() {

        var $roomStyleItems = document.getElementById('roomStyleItems');
        if ($roomStyleItems) {
            var $btnMore = document.getElementById('showMoreRooms');
            var $inner = $roomStyleItems.querySelectorAll('.room-style__inner');

            $roomStyleItems.addEventListener('keydown', keyEventsPopup, false);

            for (var m = 0; m < $inner.length; m++) {
                $inner[m].addEventListener('click', function (e) {
                    popup(e);
                }, false);
            }

            if (!isPhone()) {
                var $invisibleRoomItems = $roomStyleItems.querySelectorAll('.room-style__item:nth-child(n + 5)');
                for (var i = 0; i < $invisibleRoomItems.length; i++) {
                    var els2 = $invisibleRoomItems[i].querySelector('.room-style__inner');
                    els2.tabIndex = "-1";
                }
            }

            if ($btnMore) {
                $btnMore.addEventListener('click', toggleGallery, false);
                $btnMore.addEventListener('keydown', keyEventsShowMore, false);
            }
        }
    }

